package se.najah.edu;

public class product {
	private String item;
	private int price;
	private int count;
	
	public String getitem() {
		return item;
	 }

	public void setitem(String Item) {
	    item=Item;
	}
	
	public int getprice() {
		return price;
	}

	public void setprice(int Price) {
		price=Price;
	}
	public int getcount() {
		return count;
	}

	public void setcount(int Count) {
		count=Count;
	}
}
